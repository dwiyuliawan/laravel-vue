<?php
    $s = 9;
    $l = 8;
    $t = 7;
    $p = 5;

    //1. volum kubus = s*s*s
    $volume_kubus = $s*$s*$s;
    echo "$s*$s*$s = $volume_kubus";
    echo "<hr>";
    //2. volum limas = 1/3*l*t
    $volume_limas = 1/3*$l*$t;
    echo "1/3*$l*$t = $volume_limas";
    echo "<hr>";
    //3. volume balok = p*l*t
    $volume_balok = $p*$l*$t;
    echo "$p*$l*$t = $volume_balok";
    echo "<hr>";
?>