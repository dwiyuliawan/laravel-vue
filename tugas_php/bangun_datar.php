<?php

    $s = 5;
    $p = 7;
    $l = 8;
    $a = 6;
    $t = 4;
    $j = 10;

    //1. luas persegi = s*s
    $luas_persegi = $s*$s;
    echo "$s*$s = $luas_persegi";
    echo "<hr>";
    //2. luas persegi panjang = p*l
    $luas_persegi_panjang = $p*$l;
    echo "$p*$l = $luas_persegi_panjang";
    echo "<hr>";
    //3. luas segi tiga = 1/2*a*t
    $luas_segi_tiga = 1/2*$a*$t;
    echo "1/2*$a*$t = $luas_segi_tiga";
    echo "<hr>";
    //4. luas lingkaran = 22/7*j*j
    $luas_lingkaran = 22/7*$j*$j;
    echo "22/7*$j*$j = $luas_lingkaran";
    echo "<hr>";
    //5. luas jajar genjang = a*t
    $luas_jajar_genjang = $a*$t;
    echo "$a*$t = $luas_jajar_genjang";
    echo "<hr>";

?>